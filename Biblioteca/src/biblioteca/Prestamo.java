    private void prestAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prestAceptarActionPerformed
        String libro = prestLibro.getText();
        String usuario = prestUsuario.getText();
        Usuario auxUsuario = new Usuario();
        boolean encontradoU = false;
        boolean encontradoL = false;

        for (Usuario usuarioTemp : usuarios) {
            if (usuario.equals(usuarioTemp.obtenerCedula())) {
                auxUsuario = usuarioTemp;
                encontradoU = true;
                break;
            } else {
                encontradoU = false;
            }
        }

        if (!encontradoU) {
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el usuario");
        }

        for (Libro libroTemp : libros) {
            if (libro.equals(libroTemp.obtenerCodigo()) && libroTemp.obtenerDisponibilidad()) {
                libroTemp.prestar(auxUsuario);
                encontradoL = true;
                break;
            } else {
                encontradoL = false;
            }
        }

        if (!encontradoL) {
            JOptionPane.showMessageDialog(this, "Libro no disponible");
        }
        if (encontradoU && encontradoL) {
            JOptionPane.showMessageDialog(this, "Se realizo ha realizado el prestamo del libro");
        }


    }//GEN-LAST:event_prestAceptarActionPerformed

